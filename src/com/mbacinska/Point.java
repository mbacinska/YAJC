package com.mbacinska;

public class Point {

    double x;
    double y;

    double getR(){
        return Math.sqrt(x*x+y*y);
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
